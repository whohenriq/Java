package Basic;
/*
 Em Java, é necessário declarar o tipo de dado que uma variável vai armazenar

int: números inteiros, como 1, 42, -7.
double: números decimais, como 3.14, 0.001.
char: caracteres individuais, como 'A', 'b'.
boolean: valores lógicos, true ou false.

*/

public class DataTypes {
    public static void main(String[] args) {
        int number = 10;
        double pi = 3.14159;
        char letter = 'A';
        boolean isJavaValue = false;

        System.out.println(number);
        System.out.println(letter);
        System.out.println(pi);
        System.out.println(isJavaValue);
    }
}
